#include <iostream>
using namespace std;

int main()
{
    //explicit type casting -> it is done by the user

    int f = 8;
    double g = (double)f;
    cout << g << endl;
    float a = (float)f;
    cout << a << endl;

    double c = 6.44444;
    int d = (int)c;
    cout << d << endl;
    int m =97;
    char n = (char)m;
    cout << n << endl;

    //implicit type casting -> it is done by the system automatically
}